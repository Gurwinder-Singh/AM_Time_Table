    public void setKey(String key) {
        this.key = key.trim();
        if (key.trim().equals("MODIFY_SUBJECT")) {
            lbl1.setText("Name");
            lbl2.setText("Alias");
            lbl3.setText("Id");
            combo.setVisible(false);
            lbl4.setVisible(false);
            txt4.setVisible(false);
            lbl5.setVisible(false);
            setTitle("Modify Subject");
            jPanel1.setBorder(Utility.getBorder("Modify Subject", jPanel1));
            pack();
        }
    }

    public void setInfo(Vector info) {
        this.info = info;
        if(key.equals("MODIFY_SUBJECT")){
            SubjectDetail detail = (SubjectDetail) info.elementAt(0);
            txt1.setText(detail.getName());
            txt2.setText(detail.getAlias());
            txt3.setText(detail.getSub_id());
        }
    }

