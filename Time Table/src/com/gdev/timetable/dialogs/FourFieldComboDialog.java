    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        if (key.equals("MODIFY_SUBJECT")) {
            SubjectDetail detail = (SubjectDetail) info.elementAt(0);
            detail.setName(txt1.getText());
            detail.setAlias(txt2.getText());
            detail.setSub_id(txt3.getText());
            Result r = DbManager.getDefault().modifySubject(detail);
            if (r.isSuccess()) {
                MessageDisplay.showSuccessDialog(null, "Subjects Modify Succesfully", "Success");
                clear();
            } else {
                MessageDisplay.showErrorDialog(null, r.getMessage());
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed


